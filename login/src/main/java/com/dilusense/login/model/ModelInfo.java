package com.dilusense.login.model;

public class ModelInfo {
    private int id;
    private String name;
    private int user;
    private int group;
    private String videopath;
    private int state;
    private String modelpath;
    private String modelname;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getState() {
        return state;
    }

    public int getUser() {
        return user;
    }

    public int getGroup() {
        return group;
    }

    public String getVideopath() {
        return videopath;
    }

    public String getModelpath() {
        return modelpath;
    }

    public String getModelname() {
        return modelname;
    }
}
