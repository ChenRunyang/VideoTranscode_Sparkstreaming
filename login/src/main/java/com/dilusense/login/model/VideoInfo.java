package com.dilusense.login.model;

public class VideoInfo {
    private String name;
    private int user;
    private int group;
    private int model;
    private String videopath;
    private int state;
    private String serverpath;
    public String getName() {
        return name;
    }

    public int getUser() {
        return user;
    }

    public int getGroup() {
        return group;
    }

    public int getModel() {
        return model;
    }

    public String getVideopath() {
        return videopath;
    }

    public int getState() {
        return state;
    }

    public String getServerpath() {
        return serverpath;
    }
}
